'''1. Kiểu số học (Numeric Types)

int: số nguyên (ví dụ: 10, -5, 1000)

float: số thực dấu chấm động (ví dụ: 3.14, -0.5, 2.0)

complex: số phức (ví dụ: 2+3j, -1j)

🔹 2. Kiểu chuỗi (Text Type)

str: chuỗi ký tự (ví dụ: "Hello", 'Python')

🔹 3. Kiểu logic (Boolean Type)

bool: chỉ có hai giá trị True hoặc False

🔹 4. Kiểu dãy (Sequence Types)

list: danh sách, có thể thay đổi (mutable)
Ví dụ: [1, 2, 3], ["apple", "banana", "cherry"]

tuple: bộ, không thay đổi (immutable)
Ví dụ: (1, 2, 3), ("red", "green", "blue")

range: dãy số, thường dùng trong vòng lặp
Ví dụ: range(5) sinh ra các số từ 0 đến 4

🔹 5. Kiểu tập hợp (Set Types)

set: tập hợp, không chứa phần tử trùng lặp, không có thứ tự
Ví dụ: {1, 2, 3}, {"a", "b", "c"}

frozenset: giống set nhưng bất biến (immutable)

🔹 6. Kiểu ánh xạ (Mapping Type)

dict: từ điển, lưu trữ theo cặp key: value
Ví dụ: {"name": "Huy", "age": 21}

🔹 7. Kiểu nhị phân (Binary Types)

bytes: dãy byte bất biến

bytearray: dãy byte có thể thay đổi

memoryview: đối tượng bộ nhớ, cho phép truy cập dữ liệu nhị phân'''